// Question 1
const answer_faq1 = document.getElementById("toggleAnswerFaq1");
const buttonAnswerFaq1 = document.getElementById("buttonToggleAnswerFaq1");

if(document.body.contains(answer_faq1)) {
  buttonAnswerFaq1.addEventListener("click", (event) => {
    if(answer_faq1.style.display === "block"){
      answer_faq1.style.display = "none"
    }
    else {
      answer_faq1.style.display = "block"
    }
  });
}

